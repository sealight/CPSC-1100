/**
 * This is class integerSquares, which will calculate the squares of the
 * integers 1-9.
 *
 * @author Tyler Benjamin
 * @version 10/3/17
 */

public class integerSquares
{
    public static void main(String args[]){
        int x = 1;
        int x_Squared1 = (x*x); // 1*1 = 1
        System.out.println(x_Squared1);
        
        x++;
        int x_Squared2 = (x*x);  // 2*2 = 4
        System.out.println(x_Squared2);
        
        x++;
        int x_Squared3 = (x*x);  // 3*3 = 9
        System.out.println(x_Squared3);
        
        x++;
        int x_Squared4 = (x*x);  // 4*4 = 16
        System.out.println(x_Squared4);
        
        x++;
        int x_Squared5 = (x*x);  // 5*5 = 25
        System.out.println(x_Squared5);
        
        x++;
        int x_Squared6 = (x*x);  // 6*6 = 36
        System.out.println(x_Squared6);
        
        x++;
        int x_Squared7 = (x*x); // 7*7 = 49
        System.out.println(x_Squared7);
        
        x++;
        int x_Squared8 = (x*x); // 8*8 = 64
        System.out.println(x_Squared8);
        
        x++;
        int x_Squared9 = (x*x); // 9*9 = 81
        System.out.println(x_Squared9);
    }
}
